import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const inputDir = path.join(root, "data", "sofia", "catalog-v2-districts");
const outputPath = path.join(root, "data", "sofia", "mzm-catalog.json");

const districtFiles = [
  ["Банкя", "bankya.json"],
  ["Витоша", "vitosha.json"],
  ["Връбница", "vrabnitsa.json"],
  ["Възраждане", "vazrazhdane.json"],
  ["Изгрев", "izgrev.json"],
  ["Илинден", "ilinden.json"],
  ["Искър", "iskar.json"],
  ["Красна поляна", "krasna-polyana.json"],
  ["Красно село", "krasno-selo.json"],
  ["Кремиковци", "kremikovtsi.json"],
  ["Лозенец", "lozenets.json"],
  ["Люлин", "lyulin.json"],
  ["Младост", "mladost.json"],
  ["Надежда", "nadezhda.json"],
  ["Нови Искър", "novi-iskar.json"],
  ["Оборище", "oborishte.json"],
  ["Овча купел", "ovcha-kupel.json"],
  ["Панчарево", "pancharevo.json"],
  ["Подуяне", "poduyane.json"],
  ["Сердика", "serdika.json"],
  ["Слатина", "slatina.json"],
  ["Средец", "sredets.json"],
  ["Студентски", "studentski.json"],
  ["Триадица", "triaditsa.json"]
];

const expectedCounts = {
  "Банкя": 3,
  "Витоша": 17,
  "Връбница": 10,
  "Възраждане": 7,
  "Изгрев": 10,
  "Илинден": 8,
  "Искър": 13,
  "Красна поляна": 11,
  "Красно село": 26,
  "Кремиковци": 12,
  "Лозенец": 16,
  "Люлин": 23,
  "Младост": 25,
  "Надежда": 21,
  "Нови Искър": 11,
  "Оборище": 13,
  "Овча купел": 15,
  "Панчарево": 11,
  "Подуяне": 17,
  "Сердика": 10,
  "Слатина": 16,
  "Средец": 11,
  "Студентски": 9,
  "Триадица": 26
};

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

const rawEntries = [];
const ids = new Set();
const districts = [];
const errors = [];

for (const [district, fileName] of districtFiles) {
  const filePath = path.join(inputDir, fileName);
  if (!fs.existsSync(filePath)) {
    errors.push(`Missing district file: ${fileName}`);
    continue;
  }

  const districtCatalog = readJson(filePath);
  if (districtCatalog.district !== district) {
    errors.push(`${fileName}: expected district '${district}', got '${districtCatalog.district}'`);
  }

  const entries = Array.isArray(districtCatalog.entries) ? districtCatalog.entries : [];
  if (entries.length !== expectedCounts[district]) {
    errors.push(`${district}: expected ${expectedCounts[district]} entries, got ${entries.length}`);
  }

  districts.push(district);

  for (const [index, entry] of entries.entries()) {
    const label = `${district}/${index + 1}`;
    if (!entry.id) errors.push(`${label}: missing id`);
    if (!entry.name) errors.push(`${label}: missing name`);
    if (!entry.district) errors.push(`${label}: missing district`);
    if (entry.district !== district) errors.push(`${entry.id || label}: district mismatch '${entry.district}' !== '${district}'`);
    if (!entry.address) errors.push(`${entry.id || label}: missing address`);
    if (entry.id && ids.has(entry.id)) errors.push(`${entry.id}: duplicate id`);
    if (entry.id) ids.add(entry.id);

    rawEntries.push({
      ...entry,
      source: entry.source || "manual-verified-catalog-v2-2026-05-02"
    });
  }
}

if (districts.length !== 24) errors.push(`Expected 24 districts, got ${districts.length}`);
if (rawEntries.length !== 341) errors.push(`Expected 341 raw entries, got ${rawEntries.length}`);

if (errors.length > 0) {
  console.error("Catalog v2 build failed:");
  for (const error of errors) console.error(`- ${error}`);
  process.exit(1);
}

const catalog = {
  generatedAt: new Date().toISOString(),
  source: "manual verified catalog v2 rebuilt district by district from latest PDF / ISODZ list",
  schemaVersion: "mzm.catalog.v2.manual",
  notes: [
    "This file is generated by scripts/build-catalog-v2.mjs from data/sofia/catalog-v2-districts/*.json.",
    "One raw entry equals one real selectable option from the official list.",
    "Mixed kindergarten/nursery records are represented once with hasNurseryGroups=true.",
    "Runtime must read only this final mzm-catalog.json file."
  ],
  districts,
  rawEntries
};

fs.writeFileSync(outputPath, `${JSON.stringify(catalog, null, 2)}\n`, "utf8");
console.log(`Built Sofia catalog v2: ${districts.length} districts, ${rawEntries.length} entries.`);
console.log(`Output: ${path.relative(root, outputPath)}`);
